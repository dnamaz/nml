def main():
    rules_path = sys.argv[1] if len(sys.argv) > 1 else "tax_rules_2024.json"

    with open(rules_path) as f:
        rules = json.load(f)

    print("=" * 64)
    print(f"  Rule Transpiler — {rules['name']}")
    print("=" * 64)
    print()

    # Transpile
    transpiler = TaxRuleTranspiler(rules)
    nml_program = transpiler.transpile()

    instr_count = transpiler.p.instruction_count
    print(f"  [1/3] Transpiled: {instr_count} NML instructions")

    nml_path = "tax_rules_calc.nml"
    with open(nml_path, "w") as f:
        f.write(nml_program)
    print(f"         Wrote: {nml_path}")
    print()

    # Generate test data files and compute expected results
    print(f"  [2/3] Analytical validation:")
    print()
    print(f"  {'Employee':<45} {'Net/Period':>12} {'Fed Tax':>12} {'FICA':>12}")
    print(f"  {'-'*45} {'-'*12} {'-'*12} {'-'*12}")

    for emp in TEST_EMPLOYEES:
        result = compute_tax_analytical(emp["data"], emp["pay_periods"], rules)
        print(f"  {emp['name']:<45} "
              f"${result['net_pay']:>10,.2f} "
              f"${result['federal_tax']:>10,.2f} "
              f"${result['fica_tax']:>10,.2f}")

    # Write test data file for the last employee tested
    for emp in TEST_EMPLOYEES:
        data_file = generate_data_file(emp["data"], emp["pay_periods"])
        fname = "tax_rules_test.nml.data"
        with open(fname, "w") as f:
            f.write(data_file)

